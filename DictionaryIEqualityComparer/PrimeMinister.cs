using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace DictionaryIEqualityComparer
{
    class PrimeMinister
    {
        public string Name { get; private set; }
        public int YearElected { get; private set; }

        public PrimeMinister(string name, int yearElected)
        {
            this.Name = name;
            this.YearElected = yearElected;
        }

        /* ToString implementation taht displays two fields */
        public override string ToString()
        {
            return string.Format("{0}, Year Elected: {1}", Name, YearElected);
        }

    }
}
